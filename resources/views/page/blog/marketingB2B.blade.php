					<h2 class="h2 post__title entry-title ">
						<a href="#">Significado</a>
					</h2>
					<p class="post__text">
                        En la agencia de marketing digital y publicidad nébula sabemos que desde siempre han existido dos tipos de marketing. El B2B marketing y el B2C. El primero es aquel que desarrollan los negocios que ofrecen productos o servicios a otros negocios o empresas. Y el segundo es el que utilizan los que se dirigen al consumidor final, tienen en cuenta los hábitos del consumidor.
                        <br>Con lo que, el significado del marketing B2B, podría resumirse en una frase muy simple; estrategia que se implementa para captar la atención de empresas o negocios.
                    </p>
				</div>
				<div>
					<h2 class="h2 post__title entry-title ">
						<a href="#">¿Qué diferencia hay entre el B2B marketing y el B2C?</a>
					</h2>
					<p class="post__text">
                        En ambos casos, lo que se busca es generar y mantener la confianza con el público objetivo para que finalmente convierta. Lo que ocurre es que el proceso de toma de decisión suele ser más largo cuando hablamos de B2B marketing. Y ahí está la principal diferencia.
                        <br>En nébula te recomendamos que al diseñar una estrategia de marketing B2B, es necesario tener esto en cuenta y planear acciones que acompañen al lead todo el tiempo necesario hasta obtener una respuesta.
                        <br>Es importante hacer hincapié en este aspecto, ya que la empresa puede estar comparando opciones y, si la descuidamos, puede terminar contratando a otra marca.
                    </p>
				</div>
				<div>
					<h2 class="h2 post__title entry-title ">
						<a href="#">¿Qué es B2B en marketing?</a>
					</h2>
					<p class="post__text">
                        B2B son las siglas de “Business to Business”, lo cual se podría traducir a “negocios a negocios”.
                        <br>Esto, en pleno proceso de digitalización, ha llevado a pensar que la mejor forma de captar clientes en este entorno es usando plataformas como LinkedIn.
                        <br>Sin embargo, no hay que perder de vista que siempre estamos vendiendo a personas. Y que estas hoy en día están hiper conectadas. Por ello una estrategia de B2B marketing debería de mirar más allá y acercarse a sus potenciales clientes allí donde estén. Siempre hay que trabajar bajo esta premisa.
                    </p>
				</div>	
				<div>
                    <h2 class="h2 post__title entry-title ">
						<a href="#">Desafíos actuales en el Marketing B2B</a>
					</h2>
                    <ul>
						<li>Mejorar la eficiencia de los ciclos de venta.</li>
						<li>Integrar y digitalizar las áreas de marketing y ventas.</li>
						<li>Optimizar el Coste de Adquisición de nuevos clientes en este entorno.</li>
						<li>Mantener o potenciar la experiencia del usuario.</li>
