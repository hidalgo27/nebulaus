				<div>
					<p class="c-dark">
                        La diferenciación en el mercado en estos momentos es de vital importancia, ya que implica de una manera directa la propia existencia de la marca porque si no la marca puede llegar a no distinguirse de las otras y de esta manera tener una existencia plana donde al final no se llegan a conseguir los objetivos marcados, como por ejemplo estar en la cabeza de nuestro público.
                    </p>
					<p class="c-dark">
						Esta identidad que se construye día a día hay que hacerla de una manera coherente con los valores que tenemos y que queremos resaltar como marca. Por ejemplo, de nada me vale cantar las virtudes de valores relacionados con el medioambiente y no ser honesto en los procesos que se siguen para conseguir el producto. Como se suele decir se caza antes a un mentiroso que, a un cojo, por esta razón hay que ser transparentes con nuestro público y guardar una relación lógica y consecuente con los valores que queremos destacar.    
                    </p>
				</div>
				<div>
					<p class="c-dark">
                        Como bien es sabido la identidad de la marca se puede demostrar de muchas maneras, desde el logotipo propio de la marca hasta su tipografía o los propios colores corporativos de la propia marca. De esta manera no se realiza ninguna estrategia de branding si no está bien pensada y desarrollada para la marca.
					</p>
					<p class="c-dark">
						En este sentido hay que ser conscientes y estar muy atentos ya que a día de hoy los distintos escenarios que tiene una marca han de estar controlados. Esta diferencia en los medios para transmitir el mensaje debe de ir acorde en todos sus puntos de la marca y expresar de esta manera cómo queremos ser vistos
                    </p>
				</div>
				<div>					
					<p class="c-dark">
                        En la agencia de marketing y publicidad nébula cuando aplicamos el branding tenemos que hacernos preguntas. Por ejemplo, ¿Quiénes somos? o ¿Cómo nos ve nuestro público? ¿Cómo queremos que nos vea? y una pregunta que consideramos de especial importancia sería ¿Dónde queremos llegar? Al hacernos este tipo de cuestiones podemos saber qué tipo de estrategia debemos adoptar marcando de una manera clara todos los puntos que podemos mejorar para llegar a la meta que nos planteamos como marca.
                    </p>
					<p class="c-dark">
						De este modo podremos establecer un camino claro que realizando las diferentes tácticas que nos irán acercando más a donde queremos llegar. En este sentido debemos ser conscientes de quiénes somos y cuáles son las acciones que más nos sitúan en la línea que hemos decidido seguir. Lo que queremos decir con esto es que pese a estar en el mismo escenario que una marca reconocida no debemos copiar las acciones que esta marca tiene en su desarrollo, sino que tendremos que elaborar las nuestras propias en función a los objetivos realista que planteamos. 
                    </p>
					<p class="c-dark">
						Existen diferentes agencias y consultorías que están especializadas en este tipo de estrategias como por ejemplo la agencia de marketing y publicidad “NÉBULA”, ya que no significa solamente vender, sino que hay objetivos más sutiles detrás de todas las acciones realizadas.    
                    </p>
