                    <p class="c-dark">El branding se puede definir como el proceso de construcción de una marca. Se busca dar peso y mostrar todas las cualidades que tiene la marca en sí para que el cliente pueda conocer los valores y visión que la marca tiene y que le sirve para diferenciarse de otras.</p>
