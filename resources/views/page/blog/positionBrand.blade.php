				<div>
					<h2 class="h2 post__title entry-title ">
						<a href="#">CONTENIDO DE VALOR PARA POSICIONAR TU MARCA DIGITAL</a>
					</h2>
					<p class="post__text">
						Tienes que tener claro que es lo que tu cliente desea y cómo le puedes ayudar a obtenerlo, te será de mucha utilidad al momento de contactar con él.
						<br>Ya sea que decidas utilizar como canal de comunicación una página web, redes sociales o ambas para dar a conocer digitalmente tu marca, es crucial que el contenido que muestres sea de valor, que aporte algo nuevo, interesante, innovador, entretenido, etcétera.
						<br>En nébula se aplican estas preguntas que te ayudarán a crear contenido de valor son:
					</p>
					<ul>
						<li>¿Cuál es tu objetivo como negocio?</li>
						<li>¿Por qué tu cliente ideal debería de comprarte a ti y no a tu competencia?</li>
						<li>¿Sabes dónde encontrar a ese cliente y cómo atraerlo a tu cuenta?</li>
						<li>¿Tu adquirirías tu propio servicio o producto? ¿Por qué?</li>
					</ul>
				</div>	
				<div>
					<p class="post__text">
						Todo lo que anteriormente mencionado en este artículo debe de ir alineado a una estrategia digital efectiva. Si realmente quieres posicionar tu marca digital, todo lo que publiques y compartas debe de ser congruente entre sí, siguiendo los valores de tu negocio, los objetivos que quieres alcanzar y el público al que te dirijas.
					</p>
