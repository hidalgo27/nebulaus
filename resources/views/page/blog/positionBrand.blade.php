				<div>
					<h2 class="h2 post__title entry-title ">
						<a href="#">¿COMO POSICIONAR TU MARCA DIGITAL?</a>
					</h2>
					<p class="post__text">
						La agencia de marketing y publicidad Nébula recomienda que antes que cualquier otro paso, debes de tener muy claro quién es tu cliente ideal. Tener bien identificado sus características demográficas, sociales, económicas, gustos y preferencias. Después, algo que te ayudará a conocerlo a fondo es realizar la Metodología 3D: Dolores, Deseos y Dificultades. Para ello plantéate las siguientes preguntas:
					</p>
					<ul>
						<li>¿Cuáles son los DOLORES o problemas que tiene mi cliente ideal y cómo puedo resolverlo con mi producto o servicio?</li>
						<li>¿Qué es lo que mi cliente ideal DESEA, quiere conseguir o lograr? ¿Cuál sería la situación más deseable para él al adquirir mi producto?</li>
						<li>¿Cuáles son las DIFICULTADES que mi cliente ideal no ha logrado resolver?</li>
					</ul>
					<p>
					Una vez que hayas realizado este ejercicio, tendrás claro cómo puedes solucionar esa problemática o necesidad de tu cliente por la cual estarían dispuestos a darte su dinero.
					</p>
				</div>
				<div>
					<h2 class="h2 post__title entry-title ">
						<a href="#">TU PRODUCTO </a>
					</h2>
					<p class="post__text">
						Muchas personas que venden sus productos les cuesta definir de manera concreta que es lo que venden, más aún si son bienes intangibles. Por ello aquí te dejo unas preguntas para que puedas reflexionar sobre tus ventas. 
					</p>
					<ul>
						<li>¿Cuál es el nombre de tu producto o servicio?</li>
						<li>¿Cómo definirías tu producto?</li>
						<li>¿Cuál es el principal problema (o necesidad) que resuelve?</li>
						<li>¿Qué hace único a tu producto?</li>
						<li>¿Qué beneficios obtiene tu cliente al adquirir tu producto?</li>
					</ul>
					<p class="post__text">
						Si es sencillo para ti responder estas preguntas en pocas palabras, quiere decir que tienes todo muy claro y tus argumentos de venta serán sólidos. Por el contrario, si te fue complicado, si no encuentras una respuesta breve y concreta a cada una de estas preguntas, será muy útil que te tomes el tiempo de reflexionarlo y trabajar en ello.
					</p>
					<div class="testimonial-item quote-left bg-border-color">
						<div class="h5 testimonial-text">
							<span class="c-primary">Consejito Nébula:</span> 
							<br><span> Es muy importante que lo que ofrezcas a tu publico este muy bien delimitado.                      
						</div>
						<div class="quote">
							<i class="seoicon-lightbulb-on c-secondary"></i>
						</div>
