					</h2>
					<ul>
						<li>
							El contenido en formato de audio te permite optimizar el tiempo tanto si eres oyente o eres el que los creas.
						</li>
						<li>
							Mantienes tu cerebro ocupado con toda la información que vas procesando, porque todo lo que dice la persona del otro lado lo vas recreando.
						</li>
						<li>
							En su mayoría son de fácil acceso y gratuito, es decir: formación gratuita.
						</li>
						<li>
							No tienes un horario establecido, porque puedes ir y escucharlo cuando tú puedas.
						</li>
					</ul>
