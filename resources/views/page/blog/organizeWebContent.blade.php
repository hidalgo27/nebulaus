					<img class="pb30" loading="lazy" src="img/blog-details.jpg" alt="seo">
					<p class="post__text">
                        Cuando pensamos en cómo organizar los contenidos de una nueva web o en la reestructuración de nuestra web actual, debemos realizar un análisis previo el cual nos ayudara a decidir mejor cómo debemos enfocar la organización de nuestros contenidos. Te compartiré 4 cosas que en La Agencia de Marketing y Publicidad “Nébula” tomamos en cuenta, estas son:
                    </p>
                    <ul>
                        <li>
                            <h2 class="h2 post__title entry-title ">
                                <a href="#">CUAL ES EL OBJETIVO</a>
                            </h2>
                            <p class="post__text">
                                El error que la mayoría de las personas cometemos a la hora de organizar el contenido de nuestra web es no pararnos a pensar que es lo que queremos conseguir con ella. En Nébula debemos tener claro desde el principio qué tipo de web vamos a tener y cuál es nuestro objetivo. Por ejemplo, Si lo que tenemos es una web corporativa, nuestro objetivo principal es la reputación de nuestra marca, por lo que adquiere más relevancia la línea editorial adoptada a través de nuestros contenidos y, por tanto, los valores de la marca deben estar presentes en la estructuración que se lleve a cabo.    
                            </p>
                        </li>
                        <li>
                            <h2 class="h2 post__title entry-title ">
                                <a href="#">EL PUBLICO AL QUE TE DIRIGES</a>
                            </h2>
                            <p class="post__text">
                                A la hora de estructurar nuestro contenido debemos pensar a quién nos vamos a dirigir. En este momento, es importante el diseño de nuestro público potencial o usuario ideal que va a consumir nuestro contenido ya que solamente al entender cómo se comporta e interacciona con nuestro sitio web, vamos a poder ofrecerle soluciones en forma de nuestros contenidos que sea lo que espera de nuestro servicio o marca.
                                <br>Este conjunto de interacciones de nuestro usuario con nosotros a través de nuestra web (y otros canales digitales), es lo que se conoce como <b>viaje del usuario digital</b>, por lo que es fundamental organizar nuestro contenido en función de sus expectativas y su comportamiento de navegación, creando una buena experiencia.
                                <br>Por ejemplo, debemos considerar la utilización de más contenido de tipo audiovisual en nuestra web si nuestro público objetivo se compone principalmente de nuevas generaciones.
                            </p>
                        </li>
                        <li>
                            <h2 class="h2 post__title entry-title ">
                                <a href="#">ALINEACIÓN CON LA ESTRATEGIA DEL NEGOCIO </a>
                            </h2>
                            <p class="post__text">
                                Es común que en nuestra web presentemos los productos o servicios de nuestro negocio. Por esto, En nébula es importante que la estrategia esté presente a la hora de establecer el árbol de navegación, situando las líneas de producto estratégicas a un primer nivel de navegación. Debemos asegurarnos que la navegación del usuario sea fácil e intuitiva. Para ello, debemos analizar nuestros productos y servicios y así poder organizar los contenidos partiendo de una estructura de contenido piramidal. 
                                <br>Todo esto sin perder de vista el equilibrio en el tamaño de las distintas categorías que tenga nuestro sitio.    
                            </p>
                        </li>
                        <li>
                            <h2 class="h2 post__title entry-title ">
                                <a href="#">IMPORTANCIA DEL “SEO” (optimización de motores de búsqueda)</a>
                            </h2>
                            <p class="post__text">
                                La organización del contenido deberá tener en cuenta a los buscadores como parte de esta estrategia. Por ejemplo, las páginas de contenidos claves deben reflejar el objetivo o misión de nuestra web.
                                <br>Cuanta más lógica sea la estructuración de nuestro contenido, más fácil será para los buscadores saber cuál es el contenido más relevante, lo que ayudará a un mejor posicionamiento SEO.
                            </p>
                        </li>
                    </ul>
                    <p>
                        Resulta importante señalar que todos los puntos enumerados se deben de tener en cuenta a la hora de organizar nuestros contenidos en la web ya que estos van interrelacionados y debemos lograr un equilibrio entre ellos para que la organización de nuestros contenidos sea un éxito. 
                    </p>
